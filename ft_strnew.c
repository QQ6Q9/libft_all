#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *str;

	str = (char *)malloc(sizeof(*str) * (size + 1));
	if (str)
	{
		ft_memset(str, '\0', (size + 1));
		return (str);
	}
	return (NULL);
}
